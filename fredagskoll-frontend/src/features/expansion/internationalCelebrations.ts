import { CelebrationCategory } from '../celebrations/celebrationDefinitions';
import { Locale } from '../../locale';

export type ExpansionDatasetId = 'sv-SE' | 'en-US';

type FixedDateRule = {
  dateType: 'fixed';
  monthIndex: number;
  dayOfMonth: number;
};

type NthWeekdayRule = {
  dateType: 'nth-weekday';
  monthIndex: number;
  weekday: number;
  occurrence: number;
};

type InternationalCelebrationRule = {
  dayType: string;
  category: CelebrationCategory;
  score: number;
  rule: FixedDateRule | NthWeekdayRule;
  copy: Record<
    Locale,
    {
      title: string;
      subtitle: string;
      kicker: string;
    }
  >;
};

export type InternationalCelebrationDataset = {
  id: ExpansionDatasetId;
  label: string;
  defaultLocale: Locale;
  calendarName: string;
  calendarDescription: string;
  rules: InternationalCelebrationRule[];
};

function getNthWeekdayOfMonth(
  year: number,
  monthIndex: number,
  weekday: number,
  occurrence: number
): Date {
  const firstDay = new Date(year, monthIndex, 1);
  const firstOffset = (weekday - firstDay.getDay() + 7) % 7;
  const dayOfMonth = 1 + firstOffset + (occurrence - 1) * 7;
  return new Date(year, monthIndex, dayOfMonth);
}

