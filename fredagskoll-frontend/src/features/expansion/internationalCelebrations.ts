export function getInternationalCelebrationDate(
  year: number,
  rule: InternationalCelebrationRule['rule']
): Date {
  if (rule.dateType === 'fixed') {
    return new Date(year, rule.monthIndex, rule.dayOfMonth);
  }

  return getNthWeekdayOfMonth(year, rule.monthIndex, rule.weekday, rule.occurrence);
}
